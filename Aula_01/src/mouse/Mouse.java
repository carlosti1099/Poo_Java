package mouse;

public class Mouse {
    //Atributos
    public String marca;
    public String modelo;
    public String cor;
    public int botao;
    
    public Mouse(String marca, String modelo, String cor, int botao){
        this.botao = botao;
        this.cor = cor;
        this.marca = marca;
        this.modelo = modelo;
    }
}
