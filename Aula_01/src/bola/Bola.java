package bola;

public class Bola {
    //Atributos
    String marca;
    String cor;
    String material;
    float peso;
    float diametro;
    float area;

    public Bola(String marca, String cor, String material, float peso, float diametro, float area) {
        this.marca = marca;
        this.cor = cor;
        this.material = material;
        this.peso = peso;
        this.diametro = diametro;
        this.area = area;
    }
}
